package q1;

/**
 * <p>This program prints out a stickman using the System.out.println.</p>
 *
 * @author Emilio Wu
 * @version 1.0
 */
public class Stickman {
    
    /**
     * <p>This is the main method (entry point) that gets called by the JVM.</p>
     *
     * @param args command line arguments.
     */
    public static void main(String[] args) {
        
        // Using strings to print out a stickman
        System.out.println("\t /\"\"\"\"\"\""
                + "\"\"\\");
        System.out.println("\t | (@  @) |");
        System.out.println("\t |    ^   |");
        System.out.println("\t |  \'---\' |");
        System.out.println("\t \\________/");
        System.out.println("\t_____||_____");
        System.out.println("       /\t    \\");
        System.out.println("      | /\t  \\  |");
        System.out.println("      | |\t  |  |");
        System.out.println("      \\ |\t  |  /");
        System.out.println("       \\|_________|_/");
        System.out.println("        |         |");
        System.out.println("        |    |    |");
        System.out.println("       _|    |    |_");
        System.out.println("      /______|______\\");
        
        System.out.println("\nQuestion one was called and ran sucessfully.");
    }

};
